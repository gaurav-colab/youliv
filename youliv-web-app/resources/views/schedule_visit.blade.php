@include('footer')

<script>
    $(function() {
        var top = $('.sidesticky').offset().top - parseFloat($('.sidesticky').css('marginTop').replace(/auto/, 0));
        var footTop = $('.foot-scroll').offset().top - parseFloat($('.foot-scroll').css('marginTop').replace(/auto/, 0));

        var maxY = footTop - $('.sidesticky').outerHeight();

        $(window).scroll(function(evt) {
            var y = $(this).scrollTop();
            if (y > top) {

                //Quand scroll, ajoute une classe ".fixed" et supprime le Css existant
                if (y < maxY) {
                    $('.sidesticky').addClass('fixed').removeAttr('style');
                } else {

                    //Quand la sidebar arrive au footer, supprime la classe "fixed" précèdement ajouté
                    $('.sidesticky').removeClass('fixed').css({
                        position: 'absolute',
                        top: (maxY - top) + 'px'
                    });
                }
            } else {
                $('.sidesticky').removeClass('fixed');
            }
        });
    });
</script>

